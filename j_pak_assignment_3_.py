# 10.Write a Python program to sum all the numeric items in a list?
def sum_numeric_items(my_list):
    total = 0
    for item in my_list:
        if isinstance(item, (int, float)):
            total += item
    return total

def main():
    my_list = [1, 2, 3, 4, 5, "a", "b", 6.5, 7.5, 8.5]
    result = sum_numeric_items(my_list)
    print("The sum of numeric items in the list is:", result)

