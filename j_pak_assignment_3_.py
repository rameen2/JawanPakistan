# 7. Write a program which takes 5 inputs from user for different subjectâ€™s marks, total it
# and generate mark sheet using grades?

def calculate_grade(percentage):
    if percentage >= 90:
        return "A+"
    elif percentage >= 80:
        return "A"
    elif percentage >= 70:
        return "B"
    elif percentage >= 60:
        return "C"
    elif percentage >= 50:
        return "D"
    else:
        return "F"

def main():
    try:
        marks = []
        total_marks = 0

        for i in range(5):
            subject_mark = float(input(f"Enter the marks for subject {i + 1}: "))
            marks.append(subject_mark)
            total_marks += subject_mark

        percentage = (total_marks / (5 * 100)) * 100
        grade = calculate_grade(percentage)

        print("\n------- Mark Sheet -------")
        for i in range(5):
            print(f"Subject {i + 1}: {marks[i]}")
        print(f"Total Marks: {total_marks}")
        print(f"Percentage: {percentage:.2f}%")
        print(f"Grade: {grade}")

    except ValueError:
        print("Error: Please enter valid numeric marks.")

if __name__ == "__main__":
    main()

